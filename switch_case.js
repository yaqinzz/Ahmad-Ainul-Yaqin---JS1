var pilihan = "Hijau";

switch (pilihan) {
  case "Merah":
    console.log("Anda memilih warna Merah.");
    break;
  case "Biru":
    console.log("Anda memilih warna Biru.");
    break;
  case "Hijau":
    console.log("Anda memilih warna Hijau.");
    break;
  case "Kuning":
    console.log("Anda memilih warna Kuning.");
    break;
  default:
    console.log("Warna yang Anda pilih tidak tersedia.");
}
